<script setup lang="ts">
import { computed, ref } from 'vue';
import { SButton, SDropdownMenuRadio } from '@soybeanjs/ui';
import type { MenuRadioOptionData } from '@soybeanjs/ui';

const placement = ref('top-start');

const placements: MenuRadioOptionData<string>[] = [
  { isGroupLabel: true, value: 'tooltipPlacement', label: 'Tooltip Placement' },
  { value: 'top-start', label: 'Top Start' },
  { value: 'top', label: 'Top' },
  { value: 'top-end', label: 'Top End' },
  { value: 'right-start', label: 'Right Start' },
  { value: 'right', label: 'Right' },
  { value: 'right-end', label: 'Right End' },
  { value: 'bottom-start', label: 'Bottom Start' },
  { value: 'bottom', label: 'Bottom' },
  { value: 'bottom-end', label: 'Bottom End' },
  { value: 'left-start', label: 'Left Start' },
  { value: 'left', label: 'Left' },
  { value: 'left-end', label: 'Left End' }
];

const activeLabel = computed(() => placements.find(item => item.value === placement.value)?.label);
</script>

<template>
  <div>
    <h3 class="playground-title">Radio</h3>
    <SDropdownMenuRadio v-model="placement" :items="placements">
      <template #trigger>
        <SButton variant="pure" class="w-30">{{ activeLabel }}</SButton>
      </template>
    </SDropdownMenuRadio>
  </div>
</template>
